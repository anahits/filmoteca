	<h2>SEMINARIO DE ESTÉTICA CINEMATOGRÁFICA CINE-MÁQUINA(S): LA OPERACIÓN DE LAS IDEAS-CINE</h2>
   <strong><p>Impartido por Dra. Sonia Rangel Espinosa</p></strong>
   <p><strong>Justificación:</strong></p>
   <p>Siguiendo el planteamiento deleuziano, el seminario pretende explorar las ideas-cine de Godard, Kluge, Tarr, Cronenberg, Herzog, Lynch y otros, conectando y tendiendo puentes  con algunos conceptos e ideas de la filosofía contemporánea de Deleuze, Rancière, Bataille, Blanchot y Trias.</p>
   
    <ul><strong>Objetivos</strong>
      <li>Dar herramientas teóricas y prácticas para la construcción de un análisis estético de los films. (Filosofía del cine).</li>
      <li>Reconocer las ideas (técnicas y estéticas), así como los recursos expresivos. a través de los cuales se configuran el pensamiento cinematográfico de los cineastas a revisar.</li>
      <li>Conocer y aplicar conceptos de estética cinematográfica tales cómo: imagen-movimiento, Imagen-tiempo, imagen-afección, imagen-acción, imagen-percepción, imagen-cristal, etc., entendidos como herramientas para pensar el cine.</li>
    </ul>
  <h6>PROGRAMA</h6>

  <p><strong>SESIÓN 1</strong></p>
  <p>Introducción  y presentación del seminario. Los dos aspectos del cine moderno. Cine del cerebro: Kubrick, Resnais, Godard. Cine del cuerpo. La imagen-tiempo directa. Lectura: Deleuze, Conversaciones. Trias, De cine. "Stanley Kubrick: la inteligencia y sus fantasmas".</p>

  <p>SESIÓN 2</p>
  <p>Jean-Luc Godard. Cine cuerpo-cerebro. Intersticio entre lo visual y lo sonoro. Cine-ensayo. La poética de la(s) historia(s). Cine-ensayo. Chris Marker. La historia como montaje. Otro ejemplo: Syberbeg. Lectura: Ranciére, La fabula cinematográfica; "Fábulas del cine, historias de un siglo".</p>

  <p><strong>SESIÓN 3</strong></p>

  <p>Andrej Tarkovski. La imagen poética como imagen del tiempo. La zona: imaginación, sueño, memoria. La experiencia de lo sagrado. Resonancias en el cine de Konstantin Lopushanskij. Lecturas: Tarkovski, Esculpir el tiempo. Trias, "Tarkovski: la evidencia de los sueños".</p>

  <p><strong>SESIÓN 4</strong></p>

  <p>Alexander Kluge. Manifiesto Oberhausen y nacimiento del Nuevo Cine alemán. Docu-drama, Montaje rizomático e imagen-crítica. La potencia de las imágenes-invisibles. Lectura: Rangel, "Ensayos imaginarios. La mirada de Alexander Kluge". "La historia del cine viene a nosotros desde el futuro", en : Kluge, 120 historias del cine.</p>

  <p><strong>SESIÓN 5</strong></p>

  <p>Werner Herzog. La potencia de lo falso: docu-ficción. Figuras de la existencia soberana: Fitzcarraldo y Aguirre. La conquista de lo inútil y el nacimiento del arte. Encuentros con el pensamiento de Georges Bataille. Lectura: Bataille, Lascaux o el nacimiento del arte. Rangel, "Soberanía de lo imposible".</p>

  <p><strong>SESIÓN 6</strong></p>

  <p>David Cronenberg. Imagen-viral. Cuerpo-protésico y Cuerpo intervenido. Devenires e intensidades: Interzona. Materialidad y efectos del pensamiento: lo experimental. Lectura: Rangel, "Devenires e intensidades: el cine de David Cronenberg.</p>

   <p><strong>SESIÓN 7</strong></p>

  <p>David Lynch. Imagen-fractal. Interzona y capas de virtualidad: Heterotopía. Figuras, abstracciones y presencias sonoras. Loops, bucles y líneas de fuga. Lectura: Trias, "David Lynch: ciudades y avenidas de la libido".  Rangel: "Hacia el interior de David Lynch".</p>

  <p><strong>SESIÓN 8</strong></p>

  <p>Béla Tarr. Microfísica del poder y elementos de docu-drama: la potencia del anonimato. Imagen-repetición. Ritornelos: visuales, sonoros y motrices. Elementos de la imagen-des-afección: pasividad, agotamiento y fatiga. Imagen-des-astre e imagen-apocalipsis. Lectura: Rancière, Béla Tarr. El tiempo del después. Rangel: "Una fuerza disolvente".</p>

  <p><strong>SESIÓN 9</strong></p>

  <p>Nicolás Pereda. La potencia de la repetición: Des-dramatización-des-afección. Repetición, Ensayo (retetition) y Variación. El espacio de la ficción. Lectura: Rosset, Reflexiones sobre cine; " La otra realidad". Rangel, "Imagen-repetición/imagen-des-afección. Aproximaciones al cine de Nicolás Pereda".</p>

  <p><strong>SESIÓN 10 y 11</strong></p>

  <p>Imágenes del deseo: Werner Schröeter, Rainer Werner Fassbinder, Marguerite Duras y Andreij Zulowski. Cuerpo andrógino. Heterotopía y heterocronía: inmanencia y devenir.  Intersticio entre ver y hablar: imagen-afónica, imagen-afección, imagen-espejo. Deseo y pasión. Lectura: Maniglier y Zabunyan, Foucault va al cine. Bataille, El erotismo. Deleuze, "Deseo y Placer".</p>

  <p><strong>SESIÓN 12</strong></p>

  <p>Philippe Grandrieux. Imagen-epidérmica y percepción molecular. Zoom visual y zoom sonoro. Visión haptica. El cine como máquina de la sensación. (Casting-Taylor/Paravel y Patiño) Conclusiones del seminario. Lectura: Didi-Huberman, Arde la imagen. Supervivencia de las luciérnagas.</p>

