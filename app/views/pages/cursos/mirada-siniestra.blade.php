	<h2>LA MIRADA SINIESTRA: EL CINE SEGÚN  HITCHCOCK (UN ACERCAMIENTO VOUYERISTA)</h2>
   <strong><p>Curso de análisis cinematográfico impartido  por: Juan Santiago Huerta Navarro</p>
</strong>

  <p><strong>Objetivo</strong></p>

  <p>Hacer una revisión histórica del legado e influencia de Alfred Hitchcock a través del análisis cinematográfico de sus más importantes obras,  y como  su particular visión del mundo, modificó para siempre el suspenso, el misterio, el terror, así como otros géneros cinematográficos.</p>

  <p><strong>TEMAS Y LISTA DE PELÍCULAS:</strong></p>

  <p><strong>(PRIMER TRIMESTRE) - "INICIO Y CRECIMIENTO DEL GENIO"</strong></p>
     
    <p>EL HOMBRE DETRÁS DE LA RETINA:</p>
      <ul>
        <li>Los inicios de Hitchcock en el cine. La etapa silente.</li>
          <ul>
            <p>Proyección de: "El Huésped"  ("The Lodger", 1926)</p>
          </ul>
      </ul>
     
    <p>EL ESPECTADOR QUE SABÍA DEMASIADO:</p>
      <ul>
        <li>Crecimiento y madurez de Hitchcock en el joven cine británico:</li>
          <ul>
            <p>Proyección de un fragmento de:"Hitchcock ilustrado" (entrevista de 1972).</p>
            <p>Proyección completa de: "El Hombre que sabía demasiado" ("The Man who knew too much", 1934).</p>
          </ul>
      </ul>

    <p>PISANDO EN FALSO:</p>
      <ul>
        <li>Hitchcock y la sofisticación del thriller en el cine inglés.</li>
          <ul>
            <p>Proyección de un fragmento de:"Hitchcock: los primeros años" (2005)</p>
            <p>Proyección completa de: "Los 39 Escalones" ("The 39 Steps", 1935)</p>
          </ul>
      </ul>

    <p>INOCENCIA CORRUPTA:</p>
      <ul>
        <li>La visión pesimista de la pareja en Hitchcock.</li> 
          <ul><p>Proyección de: "Joven e Inocente" ("Young and Innocent", 1937).</p>
          </ul>
      </ul>

    <p>LA OBSESIÓN DEL AMOR AUSENTE</p>
      <ul>
        <li>Pena, dolor y amor gótico. La  primera etapa de Hitchcock en el cine norteamericano.</li>
          <ul>
            <p>Proyección de un fragmento de:"El mundo gótico de Daphne Du Maurier" (2008). Proyección completa de:"Rebeca" (1940).</p>
          </ul>
      </ul> 

    <p>LA DESCONFIANZA SOSPECHOSA</p>
      <ul>
        <li>Hitchcock, romance y decepción.</li>
          <ul>
            <p>Proyección de un fragmento de: "Antes del Hecho: el sospechoso Hitchcock" (2004).</p>
            <p>Proyección completa de: "Sospecha" ("Suspicion", 1941)</p>
          </ul>
      </ul>

    <p>CRIMEN Y MONUMENTALIDAD</p>
      <ul>
        <li>Hitchcok y el paisaje sombrío.</li>
          <ul>
            <p>Proyección de "Sabotaje" ("Sabotaje, 1936) y " Saboteador" ("Saboteur", 1942).</p>
          </ul>
      </ul>       

    <p>EL ENIGMA DEL DOBLE</p>
      <ul>
        <li>Espejos y reflejos del doble en Hitchcock.</li>
          <ul>
            <p>Proyección de: "La Sombra de una duda" ("The Shadow of a doubt", 1943).</p>
          </ul>
      </ul>

    <p>LA VIDA ATRAPADA EN SÍ MISMA</p>
      <ul>
        <li>Encierro, miedo y claustrofobia en el universo Hitchcockiano.</li>
          <ul>
            <p>Proyección de: "Los Náufragos" ("Lifeboat", 1944).</p>
          </ul>
      </ul>

    <p>EL PAISAJE ONÍRICO HITCHCOCKIANO</p>
      <ul>
        <li>El universo de ensoñación criminal en Alfred Hitchcock.</li>
          <ul>
            <p>Proyección de "Cuéntame tu vida" ("Spellbound", 1945).</p>
          </ul>
      </ul>

    <p>AMOR Y TRAICIÓN</p>
      <ul>
        <li>El romance como crimen y el crimen como romance.</li>
          <ul>
            <p>Proyección de: "Tuyo es mi Corazón" ("Notorious, 1946).</p>
          </ul>
      </ul> 

     <p>EL ASESINATO Y LA MUERTE TEATRALIZADA</p>
      <ul>
        <li>Misterio y enigma del teatro en la obra de Hitchcock.</li>
          <ul>
            <p>Proyección de un fragmento de "Pánico en Escena" ("Stage Fright, 1950).</p>
            <p>Proyección completa de "La Soga" ("Rope", 1948)</p>
